public class SOA {
    public static void main(String[] args) {
        // Example array
        int[] array = { 1, 2, 3, 4, 5 };

        // Variable to hold the sum
        int sum = 0;

        // Loop through the array to calculate the sum
        for (int i = 0; i < array.length; i++) {
            sum += array[i];
        }

        // Output the sum
        System.out.println("The sum of the array is: " + sum);
    }
}
